public class expressions {
    public static void main(String[] args) {
        
        // expression = operands & operators
        // operands = values, variables, numbers, quanty
        // operators = + - * / %

        int friends = 10;
        friends = friends /3; 
        // pode ser usado outros operadores como - * / e %

        double man = 10;
        man = man / 3; // dividir por meio da "barra" divide de forma inteira, não aparece numeros com virgula para que isso aconteça podemos estar usando o doble.
        
        System.out.println(friends);
        System.out.println(man);
    }    
}
